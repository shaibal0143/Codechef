import java.io.*;
import java.util.Scanner;

public class add_2_no {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int t = scan.nextInt();
        while (t-- > 0) {
            int a = scan.nextInt();
            int b = scan.nextInt();
            System.out.println(a + b);
        }
  
    } 
}
 
